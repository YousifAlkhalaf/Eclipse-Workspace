package Assignment1;

public class FindSumMethods {
	
	public static void findSum (int a, int b) {
		System.out.printf ("%d plus %d equals %d \n", a, b, a + b);
	}
	public static void main (String[] args) {
		
		findSum(1, 3);
		findSum(3, 5);
		findSum(-7, 15);
		findSum(2, 4);
		findSum(10, 3);
		
	}
}
