public class Ex1_2_2 {

	public static void main(String[] args) {

		System.out.print("Hello World!\n");
		System.out.println("\tShut up!");
		System.out.println("\"Aww...\" said the world.");
		System.out.println("Here's a \\, as a treat.");

		/*
		 * a \n escape makes a new line, a \t escape makes a tab space, a \" escape
		 * makes a quotation mark, and a \\ escape puts a backslash
		 */
	}
}
