public class IntegerTestRoom {
	public static void main(String[] args) {
		
		int a = 5;
		int b = 2;
		
		a = a + 4;
		System.out.println("a = " + a + "\n");
		
		b = a/2;
		System.out.println("b = " + b + "\n");
		
		int f = a/b;
		System.out.println("f = " + f + "\n");
		
		a = 0;
		b = 1;
		
		int x = a % b;
		
		System.out.println("a%b = " + x);
		
	}
}
